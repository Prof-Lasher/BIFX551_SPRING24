
#' Function takes one input, adds input to itself, and returns
#' @export
#' @param x numeric variable
addSelf <- function(x) {
  #Adding the input to itself
  x + x = y
  #Returning results
  return(y)
}


#' Function takes one input, multiplies input to itself, and returns
#' @export
#' @param x numeric variable
multSelf <- function(x) {
  #Multiplying the input to itself
  x * x = y
  #Returning results
  return(y)
}
